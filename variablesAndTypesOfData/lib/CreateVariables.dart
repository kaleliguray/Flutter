void main(){

  // we can use "var" which is the type of variable that no need to specify the type

  var studentName = "Jack";
  var studentAge = 21;
  var studentHeight = 1.80;
  var studentInitial = "A";

  print(studentName);
  print(studentAge);
  print(studentHeight);
  print(studentInitial);

  //int, double, string and bool are the types of variables determined.
  // And In a print method, we can use the "$" symbol to get to variables.

  int product_id = 1234;
  String product_name = "Watch";
  int product_quantity = 100;
  double product_price = 149.99;
  String product_supplier = "Rolex";

  print("Product id : $product_id");
  print("Product name : $product_name");
  print("Product quantity : $product_quantity");
  print("Product price : $product_price");
  print("Product supplier : $product_supplier");

}